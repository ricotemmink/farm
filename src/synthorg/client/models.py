class ClientFeedback(BaseModel):
    """Feedback from a client on a task deliverable.

    Attributes:
        feedback_id: Unique feedback identifier.
        task_id: ID of the reviewed task.
        client_id: ID of the reviewing client.
        accepted: Whether the deliverable was accepted.
        reason: Explanation for the decision.
        scores: Optional per-dimension scores.
        unmet_criteria: Criteria that were not satisfied.
        created_at: Timestamp of feedback creation.
    """

    model_config = ConfigDict(frozen=True, allow_inf_nan=False)

    feedback_id: NotBlankStr = Field(
        default_factory=lambda: str(uuid4()),
        description="Unique feedback identifier",
    )
    task_id: NotBlankStr = Field(
        description="ID of the reviewed task",
    )
    client_id: NotBlankStr = Field(
        description="ID of the reviewing client",
    )
    accepted: bool = Field(description="Whether the deliverable was accepted")
    reason: NotBlankStr | None = Field(
        default=None,
        description="Explanation for the decision",
    )
    scores: dict[str, float] | None = Field(
        default=None,
        description="Optional per-dimension scores (0.0-1.0)",
    )
    unmet_criteria: tuple[NotBlankStr, ...] = Field(
        default=(),
        description="Criteria that were not satisfied",
    )
    created_at: datetime = Field(
        default_factory=lambda: datetime.now(UTC),
        description="Timestamp of feedback creation",
    )

    @model_validator(mode="after")
    def _validate_rejection_has_reason(self) -> Self:
        """Rejected feedback must include a reason."""
        if not self.accepted and self.reason is None:
            msg = "reason is required when accepted is False"
            raise ValueError(msg)
        return self

    @model_validator(mode="after")
    def _validate_scores_range(self) -> Self:
        """Ensure all scores are in [0.0, 1.0] range."""
        if self.scores is not None:
            for key, value in self.scores.items():
                if not (0.0 <= value <= 1.0):
                    msg = f"scores[{key!r}] must be in [0.0, 1.0], got {value}"
                    raise ValueError(msg)
        return self


class ClientRequest(BaseModel):
    """A client's request submitted to the intake pipeline.

    Attributes:
        request_id: Unique request identifier.
        client_id: ID of the submitting client.
        requirement: The task requirement being requested.
        status: Current intake pipeline status.
        created_at: Timestamp of request creation.
        metadata: Additional request metadata.
    """

    model_config = ConfigDict(frozen=True, allow_inf_nan=False)

    request_id: NotBlankStr = Field(
        default_factory=lambda: str(uuid4()),
        description="Unique request identifier",
    )
    client_id: NotBlankStr = Field(
        description="ID of the submitting client",
    )
    requirement: TaskRequirement = Field(
        description="The task requirement being requested",
    )
    status: RequestStatus = Field(
        default=RequestStatus.SUBMITTED,
        description="Current intake pipeline status",
    )
    created_at: datetime = Field(
        default_factory=lambda: datetime.now(UTC),
        description="Timestamp of request creation",
    )
    metadata: dict[str, Any] = Field(
        default_factory=dict,
        description="Additional request metadata",
    )

    def with_status(
        self,
        target: RequestStatus,
        **overrides: Any,
    ) -> ClientRequest:
        """Create a new request with a validated status transition.

        Args:
            target: The desired target status.
            **overrides: Additional field overrides.

        Returns:
            A new ClientRequest with the target status.

        Raises:
            ValueError: If the transition is not valid or overrides
                contain ``status``.
        """
        if "status" in overrides:
            msg = "status override is not allowed; pass transition target explicitly"
            raise ValueError(msg)
        validate_request_transition(self.status, target)
        payload = self.model_dump()
        payload.update(overrides)
        payload["status"] = target
        return ClientRequest.model_validate(payload)


class PoolConstraints(BaseModel):
    """Constraints for selecting clients from a pool.

    Attributes:
        min_strictness: Minimum strictness level for selected clients.
        max_strictness: Maximum strictness level for selected clients.
        required_domains: Domains the client must have expertise in.
        max_clients: Maximum number of clients to select.
    """

    model_config = ConfigDict(frozen=True, allow_inf_nan=False)

    min_strictness: float = Field(
        default=0.0,
        ge=0.0,
        le=1.0,
        description="Minimum strictness level",
    )
    max_strictness: float = Field(
        default=1.0,
        ge=0.0,
        le=1.0,
        description="Maximum strictness level",
    )
    required_domains: tuple[NotBlankStr, ...] = Field(
        default=(),
        description="Required expertise domains",
    )
    max_clients: int = Field(
        default=10,
        gt=0,
        description="Maximum number of clients to select",
    )

    @model_validator(mode="after")
    def _validate_strictness_range(self) -> Self:
        """Ensure min_strictness <= max_strictness."""
        if self.min_strictness > self.max_strictness:
            msg = (
                f"min_strictness ({self.min_strictness}) must be <= "
                f"max_strictness ({self.max_strictness})"
            )
            raise ValueError(msg)
        return self


