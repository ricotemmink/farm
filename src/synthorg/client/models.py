class SimulationMetrics(BaseModel):
    """Aggregated metrics from a simulation run.

    Attributes:
        total_requirements: Total requirements generated.
        total_tasks_created: Tasks created from requirements.
        tasks_accepted: Tasks accepted by clients.
        tasks_rejected: Tasks rejected by clients.
        tasks_reworked: Tasks sent back for rework.
        avg_review_rounds: Average review rounds per task.
        round_metrics: Per-round metric snapshots.
        acceptance_rate: Proportion of accepted tasks (computed).
        rework_rate: Proportion of reworked tasks (computed).
    """

    model_config = ConfigDict(frozen=True, allow_inf_nan=False)

    total_requirements: int = Field(default=0, ge=0)
    total_tasks_created: int = Field(default=0, ge=0)
    tasks_accepted: int = Field(default=0, ge=0)
    tasks_rejected: int = Field(default=0, ge=0)
    tasks_reworked: int = Field(default=0, ge=0)
    avg_review_rounds: float = Field(default=0.0, ge=0.0)
    round_metrics: tuple[dict[str, Any], ...] = Field(
        default=(),
        description="Per-round metric snapshots",
    )

    @computed_field(  # type: ignore[prop-decorator]
        description="Proportion of accepted tasks (0.0-1.0)",
    )
    @property
    def acceptance_rate(self) -> float:
        """Proportion of accepted tasks."""
        if self.total_tasks_created == 0:
            return 0.0
        return self.tasks_accepted / self.total_tasks_created

    @computed_field(  # type: ignore[prop-decorator]
        description="Proportion of reworked tasks (0.0-1.0)",
    )
    @property
    def rework_rate(self) -> float:
        """Proportion of reworked tasks."""
        if self.total_tasks_created == 0:
            return 0.0
        return self.tasks_reworked / self.total_tasks_created
