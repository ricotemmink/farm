"""Client simulation domain models.

Defines the data structures for client profiles, task requirements,
feedback, requests, and simulation metrics. All models are frozen
Pydantic models following project conventions.
"""

from datetime import UTC, datetime
from enum import StrEnum
from typing import Any, Self
from uuid import uuid4

from pydantic import BaseModel, ConfigDict, Field, computed_field, model_validator

from synthorg.core.enums import Complexity, Priority, TaskType
from synthorg.core.types import NotBlankStr  # noqa: TC001


class RequestStatus(StrEnum):
    """Lifecycle status of a client request through the intake pipeline.

    Independent from ``TaskStatus`` -- tracks the intake process
    before a task is created in the task engine.

    Transitions::

        SUBMITTED -> TRIAGING -> SCOPING -> APPROVED -> TASK_CREATED
        Any non-terminal state -> CANCELLED
    """

    SUBMITTED = "submitted"
    TRIAGING = "triaging"
    SCOPING = "scoping"
    APPROVED = "approved"
    TASK_CREATED = "task_created"
    CANCELLED = "cancelled"


VALID_REQUEST_TRANSITIONS: dict[RequestStatus, frozenset[RequestStatus]] = {
    RequestStatus.SUBMITTED: frozenset(
        {RequestStatus.TRIAGING, RequestStatus.CANCELLED}
    ),
    RequestStatus.TRIAGING: frozenset({RequestStatus.SCOPING, RequestStatus.CANCELLED}),
    RequestStatus.SCOPING: frozenset({RequestStatus.APPROVED, RequestStatus.CANCELLED}),
    RequestStatus.APPROVED: frozenset(
        {RequestStatus.TASK_CREATED, RequestStatus.CANCELLED}
    ),
    RequestStatus.TASK_CREATED: frozenset(),
    RequestStatus.CANCELLED: frozenset(),
}

_missing_request = set(RequestStatus) - set(VALID_REQUEST_TRANSITIONS)
if _missing_request:
    _msg = (
        "Missing request transition entries for: "
        f"{sorted(s.value for s in _missing_request)}"
    )
    raise ValueError(_msg)


def validate_request_transition(
    current: RequestStatus,
    target: RequestStatus,
) -> None:
    """Validate that a request status transition is allowed.

    Args:
        current: The current request status.
        target: The desired target status.

    Raises:
        ValueError: If the transition is not valid.
    """
    allowed = VALID_REQUEST_TRANSITIONS.get(current)
    if allowed is None:
        msg = f"RequestStatus {current.value!r} has no transition entry."
        raise ValueError(msg)
    if target not in allowed:
        msg = (
            f"Invalid request transition: {current.value!r} -> "
            f"{target.value!r}. Allowed: "
            f"{sorted(s.value for s in allowed)}"
        )
        raise ValueError(msg)


class ClientProfile(BaseModel):
    """Profile of a client participating in simulation.

    Attributes:
        client_id: Unique client identifier.
        name: Human-readable client name.
        persona: Description of the client's persona and behavior.
        expertise_domains: Domains the client is knowledgeable about.
        strictness_level: How strict the client is during reviews
            (0.0 = lenient, 1.0 = very strict).
    """

    model_config = ConfigDict(frozen=True, allow_inf_nan=False)

    client_id: NotBlankStr = Field(description="Unique client identifier")
    name: NotBlankStr = Field(description="Human-readable client name")
    persona: NotBlankStr = Field(
        description="Description of the client's persona",
    )
    expertise_domains: tuple[NotBlankStr, ...] = Field(
        default=(),
        description="Domains the client is knowledgeable about",
    )
    strictness_level: float = Field(
        default=0.5,
        ge=0.0,
        le=1.0,
        description="Review strictness (0.0=lenient, 1.0=strict)",
    )


