class GenerationContext(BaseModel):
    """Context provided to requirement generators.

    Attributes:
        project_id: Project to generate requirements for.
        domain: Domain area for requirement generation.
        complexity_range: Allowed complexity levels.
        count: Number of requirements to generate.
    """

    model_config = ConfigDict(frozen=True, allow_inf_nan=False)

    project_id: NotBlankStr = Field(
        description="Project to generate requirements for",
    )
    domain: NotBlankStr = Field(
        description="Domain area for requirement generation",
    )
    complexity_range: tuple[Complexity, ...] = Field(
        default=(Complexity.SIMPLE, Complexity.MEDIUM, Complexity.COMPLEX),
        description="Allowed complexity levels",
    )
    count: int = Field(
        default=1,
        gt=0,
        description="Number of requirements to generate",
    )

    @model_validator(mode="after")
    def _validate_complexity_range(self) -> Self:
        """Ensure at least one complexity level is included."""
        if not self.complexity_range:
            msg = "complexity_range must not be empty"
            raise ValueError(msg)
        return self


